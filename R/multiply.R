#' Multiply two numbers
#'
#' This function takes two numbers and returns their product.
#'
#' @param x The first number
#' @param y The second number
#' @return The product of x and y
#' @examples
#' multiply(2, 3)
#' # Returns: 6
#' @export
multiply <- function(x, y) {
  result <- x * y
  return(result)
}

